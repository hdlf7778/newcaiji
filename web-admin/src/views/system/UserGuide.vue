## 五、常见问题

**Q: 为什么某个采集源一直显示「异常」？**
A: 进入采集源详情，使用「诊断分析」查看具体原因。常见原因是网站改版导致规则失效，可直接点击「一键修复」尝试自动解决。

**Q: 一键修复显示「可能需要浏览器渲染」怎么办？**
A: 该网站是 JS 动态渲染页面。有三个选择：(1) 启动 Browser Worker：在服务器执行 \`python3 worker.py --queue browser\`；(2) 使用「人工辅助修复」，通过 F12 找到底层 API 接口后告诉系统；(3) 退役该源。

**Q: 人工辅助修复应该输入什么？**
A: 输入您通过 F12 开发者工具发现的任何有用线索。最有价值的信息是：数据 API 的 URL 和参数格式、文章列表的 CSS 选择器、特殊的请求头或 Cookie 要求。描述越具体，AI 生成的规则越准确。

**Q: 一键修复自动保存了新模板，在哪里查看？**
A: 左侧菜单 → 采集管理 → 模板列表，页面下方的「自定义模板」区域可以看到 LLM 自动创建的模板。可以编辑名称和参数，也可以删除不需要的。

**Q: 队列积压过多怎么办？**
A: 进入系统管理 → Worker 状态，确认所有 Worker 在线且心跳正常。如果 Worker 正常但积压仍多，需要扩展 Worker 实例数量。

**Q: 如何暂停某个采集源？**
A: 采集源列表 → 点击名称进入详情 → 点击「暂停」。暂停后不再参与定时调度，可随时恢复。

**Q: 导入的采集源为什么显示「待检测」？**
A: 新导入的源需完成自动检测（识别模板 + 生成规则）→ 试采验证 → 人工审核后才能上线。

**Q: LLM 相关功能不可用（LLM生成、深度分析、人工辅助修复）？**
A: 需要配置豆包大模型 API Key。请联系管理员在 Worker 的 .env 文件中配置 LLM_API_KEY。未配置时基础功能（URL修复、模板切换、多模板轮换、手动规则编辑）仍可正常使用。

**Q: 导出的文章 CSV 文件打开乱码？**
A: 导出文件使用 UTF-8 编码。在 Excel 中打开时，选择「数据」→「从文本/CSV」→ 选择 UTF-8 编码即可正确显示。

**Q: 浏览器打开后白屏或页面异常？**
A: 按 Cmd+Shift+R（Mac）或 Ctrl+Shift+R（Windows）强制刷新清除浏览器缓存。如仍有问题，清除浏览器 localStorage 后重新登录。`

