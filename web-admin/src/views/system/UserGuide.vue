## 四、日常运维流程

### 每日巡检（5 分钟）
1. 打开监控仪表盘，检查红色/黄色告警
2. 关注成功率是否 > 95%
3. 检查队列积压是否为 0
4. 查看死信队列是否有新增待处理项

### 异常处理流程

#### 第一步：发现异常

| 入口 | 查看方式 | 说明 |
|------|---------|------|
| 监控仪表盘 | 红色/黄色告警区域 | 最直观，优先看 |
| 死信队列 | 采集管理 → 死信队列 | 重试耗尽，必须人工处理 |
| 任务列表 | 筛选「失败」状态 | 正在重试中的失败任务 |

#### 第二步：判断错误类型

| 错误类型 | 典型错误信息 | 原因 | 紧急程度 |
|---------|------------|------|---------|
| parse_error | \u201C列表页提取 0 篇文章\u201D | CSS选择器规则失效 | 高 |
| template_mismatch | \u201C模板不匹配\u201D | 网站技术架构变更 | 高 |
| http_403 | \u201CHTTP 403 Forbidden\u201D | 被反爬拦截 | 中 |
| http_429 | \u201CToo Many Requests\u201D | 请求频率过高 | 中 |
| anti_bot_blocked | \u201C反爬全层级失败\u201D | 三层反爬均被拦截 | 中 |
| network_timeout | \u201CConnectTimeout\u201D | 目标网站不可达 | 低 |
| ssl_error | \u201CCERTIFICATE_VERIFY_FAILED\u201D | SSL证书异常 | 低 |

#### 第三步：按类型处理

**情况一：规则失效（parse_error）— 最常见**

表现：「列表页提取 0 篇文章」或「正文内容为空」

1. 记下采集源 ID → 规则管理 → 找到对应规则 → 点击「编辑」
2. 先点击「测试列表」确认当前规则是否失效
3. 如果列表测试失败 → 点击列表规则的「LLM生成」，等待 AI 重新分析
4. LLM 生成后再点「测试列表」确认能匹配到 ≥3 篇文章
5. 列表通过后 → 点击「测试详情」确认正文能正确提取
6. 如果详情也失败 → 点击详情规则的「LLM生成」
7. 全部通过 → 点击「保存全部规则」→ 回到死信队列点击「重试」

⚠️ 可能遇到的问题：
- LLM 生成仍不匹配 → 手动打开网站查看源代码，手动编写 CSS 选择器
- 调试过程中改乱了 → 点击「重置」按钮恢复为初始规则内容，重新开始
- 测试提示请求失败 → 检查 Python Worker API 是否在运行
- 列表通过但详情失败 → 列表规则和详情规则需要分别调试，互不影响

**情况二：网站改版（template_mismatch）**

1. 采集源详情 → 点击「重新检测」
2. 查看新模板类型是否变化（如 A→G）
3. 确认新规则 → 测试预览 → 保存
4. 点击「重置」走完整检测流程

⚠️ 可能遇到的问题：
- 检测结果没变 → 按「规则失效」处理
- 变为 G SPA渲染 → 确认 Browser Worker 在运行

**情况三：反爬拦截（http_403 / anti_bot_blocked）**

1. 浏览器手动打开网站确认是否可访问
2. 升级反爬策略：none → cookie_auto → js_fingerprint → Browser Worker
3. 严格限制 IP 的网站：配置代理池

⚠️ 可能遇到的问题：
- 同域名批量拦截 → 增大请求间隔（delay_min/delay_max）
- 代理后仍被拦截 → 更换代理服务商或使用住宅代理
- 出现 reCAPTCHA/hCaptcha → 目前无法自动处理，标记忽略

**情况四：请求频率过高（http_429）**

1. 系统设置 → 采集调度 → 增大工作时间间隔
2. 或针对特定源：增大 delay_min/delay_max（3-5 秒）
3. 等待 15-30 分钟后重试

**情况五：网络超时（network_timeout）**

- 单个源超时 → 直接重试，通常是临时问题
- 批量超时（>10 个）→ 检查 Worker 网络和 DNS
- 持续超时 → 浏览器确认网站是否关停，必要时退役该源

**情况六：SSL 证书错误（ssl_error）**

- 浏览器也报证书错误 → 等网站修复，标记忽略
- 浏览器正常但采集报错 → 检查代理是否有 SSL 中间人问题

#### 第四步：跟踪验证

1. 死信队列点击「重试」
2. 等 1-2 分钟到任务列表查看状态
3. 建议修复后连续观察 2-3 个采集周期（4-6 小时）

#### 批量异常处理技巧

1. 死信队列按「错误类型」筛选，确认是否同类问题
2. 同域名/平台批量失效 → 先修一个，确认有效后批量重试
3. 不同域名相同错误 → 检查 Worker 和网络，先重试 5-10 个观察

