## 三、各模块详细操作

### 3.1 监控仪表盘

仪表盘每 60 秒自动刷新，是日常巡检的第一入口。

- **7 个统计卡片**：活跃采集源数、待检测、待审核、异常数、今日新增文章、当前成功率、队列积压
- **需要关注区域**（三色告警）：
  - 红色 — 访问异常（HTTP 错误、连接失败）
  - 黄色 — 疑似规则失效（采集到 0 篇文章）
  - 绿色 — 长期静默（超过 30 天未采集到新文章）
- **模板健康度图表**：各模板类型的采集成功率（横向柱状图）
- **今日采集趋势图**：按时段统计采集数量（纵向柱状图）

> 建议每天上班后先看仪表盘，重点关注红色和黄色告警。

### 3.2 采集源管理

#### 列表页功能
- 顶部 **状态卡片**：点击可快速筛选（活跃/待检测/待审核/异常/暂停/退役）
- **筛选栏**：按模板、平台、地区、健康度组合筛选
- **批量操作**：勾选后可批量检测、试采、审批、暂停、退役
- **导入**：支持 CSV/Excel 批量导入

#### 采集源详情页
点击采集源名称进入详情，包含：
- **基本信息**：名称、URL、模板类型、试采评分、地区、优先级
- **运行状态**（右侧边栏）：健康度评分、最近采集状态
- **采集日志**：最近的采集记录列表
- **采集规则**：当前生效的列表规则和详情规则（JSON 格式），可点击跳转编辑
- **试采结果**：最近一次试采的文章列表，展示标题、正文预览

**可用操作按钮：**
| 按钮 | 功能 |
|------|------|
| 立即采集 | 触发一次即时采集任务（最高优先级） |
| 重新检测 | 重新检测网站模板类型和生成规则 |
| 通过/暂停/恢复 | 变更采集源运行状态 |
| 退役 | 永久停用该采集源 |

**采集源状态流转：**
待检测 → 检测中 → 检测完成 → 试采中 → 试采通过 → 待审核 → 已审批 → 活跃 ↔ 暂停 → 退役

### 3.3 规则管理
每个采集源对应一套规则，规则定义了「如何从网页中提取文章」。

#### 规则编辑页面
点击规则列表中的「编辑」进入。页面分为左右两栏：
- **顶部信息栏**：显示采集源名称（可点击跳转详情）、完整网址（可点击打开）、模板类型标签
- **左侧**：列表规则和详情规则的 JSON 编辑器
- **右侧**：对应的测试结果实时预览

#### 列表规则 (list_rule)
定义如何从列表页提取文章链接。主要字段：
- \`list_container\`：文章列表容器的 CSS 选择器
- \`list_item\`：单条文章的选择器
- \`title_selector\`：标题选择器
- \`url_selector\`：链接选择器
- \`date_selector\`：日期选择器

每个规则卡片右上角有 3 个按钮：
| 按钮 | 功能 |
|------|------|
| **测试列表** | 用当前列表规则对网站执行实际采集，右侧显示匹配到的文章列表（标题+URL+日期） |
| **LLM生成** | 调用豆包大模型重新分析网页结构，自动生成列表规则（需配置 LLM API Key） |
| **重置** | 恢复为进入页面时的初始规则内容，方便调试时回退 |

#### 详情规则 (detail_rule)
定义如何从文章详情页提取正文。主要字段：
- \`content_selector\`：正文区域选择器
- \`title_selector\`：标题选择器
- \`author_selector\`：作者选择器
- \`remove_selectors\`：需要移除的元素（如 script、nav、footer）

同样有 3 个按钮：
| 按钮 | 功能 |
|------|------|
| **测试详情** | 用列表测试中获取的第一篇文章 URL，执行详情页采集，右侧显示标题、正文预览、字数、作者 |
| **LLM生成** | 调用大模型重新分析详情页结构，生成详情规则 |
| **重置** | 恢复为初始详情规则 |

> 注意：测试详情前需先测试列表。系统需要从列表测试结果中获取一篇文章的 URL，才能执行详情页采集测试。

#### 操作流程
1. 进入规则编辑页，查看当前列表规则和详情规则
2. 点击「测试列表」验证列表规则是否能提取到文章
3. 如果提取失败 → 点击「LLM生成」让 AI 重新生成，或手动修改 JSON
4. 列表测试通过后 → 点击「测试详情」验证详情规则
5. 全部通过 → 点击底部「保存全部规则」
6. 如果调试过程中改乱了 → 点击「重置」恢复初始内容重新来

| 生成方式 | 含义 |
|---------|------|
| LLM生成 | 由豆包大模型自动分析网页结构生成（需配置 API Key） |
| 手动编写 | 运营人员手动编写的 CSS 选择器 |
| 平台适配 | 针对特定平台（如政务云）的预置规则 |

### 3.4 模板管理

模板列表页以表格形式展示所有模板，分为内置模板和自定义模板两个区域。

#### 模板列表

**内置模板（10个）：** 表格展示每种模板的名称、说明、采集队列（HTTP/Browser）、采集源数量、24h 任务数、成功率。

| 模板 | 说明 | 队列 |
|------|------|------|
| A 静态列表 | 最常见，服务端渲染的 HTML 列表页 | HTTP |
| B iframe加载 | 内容嵌套在 iframe 中 | Browser |
| C API接口 | 直接调用 JSON API | HTTP |
| D 微信公众号 | 微信公众号文章 | HTTP |
| E 搜索监控 | 搜索引擎结果页 | HTTP |
| F 登录态 | 需要登录才能访问 | Browser |
| G SPA渲染 | JavaScript 单页应用 | Browser |
| H RSS订阅 | RSS/Atom feed | HTTP |
| I 政务云 | 政府统一建站平台 | HTTP |
| J 抓包API | 抓包获取的隐藏 API | HTTP |

> HTTP 队列使用 httpx 轻量 HTTP 客户端，速度快、并发高。Browser 队列使用 Playwright 无头浏览器，能处理 JavaScript 渲染但资源消耗更大。

**自定义模板：** 由系统自动创建（一键修复中 LLM 发现新采集方案时）或手动添加。每个自定义模板基于一个内置模板的采集逻辑，并预设默认规则和反爬配置。

#### 查看采集源

点击模板名称，页面右侧展开该模板的采集源列表面板，显示所有使用此模板的采集源（名称、状态、评分、健康度、地区）。点击采集源名称可跳转到详情页。

#### 查看模板详情

点击表格中的「详情」链接，进入模板详情页，包含：

- **基本信息**：模板名称、代码、采集队列类型、采集源数量、说明
- **采集能力**（内置模板）：该模板的采集策略、支持功能、适用场景、性能特征
- **默认规则参考**（内置模板）/ **默认规则配置**（自定义模板）：列表规则、详情规则、反爬配置的完整 JSON 配置
- **采集源列表**（右侧）：使用此模板的所有采集源，支持点击跳转

#### 添加自定义模板

1. 点击页面右上角「添加自定义模板」按钮
2. 填写：模板名称、模板代码（英文唯一标识，创建后不可修改）、选择基础模板（A-J）
3. 可选填：模板说明、默认列表规则（JSON）、默认详情规则（JSON）、默认反爬配置（JSON）
4. 点击确定创建

创建后的自定义模板支持「详情」「编辑」「删除」操作。

> 提示：当一键修复中 LLM 成功发现新的采集方案时，系统会自动将该方案保存为自定义模板，可在此页面查看和管理。

### 3.5 任务列表

任务列表页面顶部有 4 个统计卡片（待处理、处理中、今日成功、今日失败），每 30 秒自动刷新。

| 状态 | 含义 |
|------|------|
| 待处理 | 已入队等待 Worker 领取 |
| 处理中 | Worker 正在执行采集 |
| 成功 | 采集完成，文章已入库 |
| 部分成功 | 列表页成功但部分详情页失败 |
| 失败 | 采集失败，将自动重试（最多 2 次） |
| 超时 | 执行超时（超过 10 分钟） |
| 死信 | 重试 2 次后仍失败，进入死信队列 |

支持按状态、模板类型、关键词筛选。失败的任务可点击「重试」重新入队。

**手动触发采集：** 点击「⚡ 立即采集」按钮，在弹窗中输入采集源 ID，任务以最高优先级立即执行。

### 3.6 死信队列
死信队列存放重试耗尽仍然失败的任务，需要人工介入。
- **重试**：修复问题后点击「重试」重新入队
- **忽略**：标记无需处理
- **批量操作**：勾选后批量重试或批量忽略

> 死信任务通常意味着规则失效或网站变更，建议先排查根因再重试。

### 3.7 文章管理

文章列表展示所有已采集入库的文章，支持：
- **筛选**：按采集源名称、地区、日期范围、关键词组合筛选
- **查看详情**：点击文章标题查看正文内容（经 HTML 安全消毒处理）
- **数据导出**：点击「导出」下载 CSV 文件（优先调用后端全量导出，若后端不可用则导出当前页数据）

### 3.8 审核工作台

审核工作台以卡片列表形式展示待审核的采集源，支持按试采评分（高/中/低）筛选。

**操作方式：**
- 点击「通过」按钮批准单个采集源上线
- 点击「拒绝」退回待修改
- 勾选多条后使用「批量通过」一键审批

### 3.9 系统管理

#### Worker 状态
实时展示所有采集 Worker 的运行状态，每 30 秒刷新一次。显示每个 Worker 的：CPU 使用率、内存占用、最近心跳时间、处理任务数。

#### 系统设置
展示系统当前的配置参数（采集调度、自动审核、告警、数据保留策略）。

> 注意：当前版本的系统设置为只读展示，修改配置需要由管理员在后端配置文件中进行。

