## 四、日常运维流程

### 每日巡检（5 分钟）
1. 打开监控仪表盘，检查红色/黄色告警
2. 关注成功率是否 > 95%
3. 检查队列积压是否为 0
4. 查看死信队列是否有新增待处理项

### 异常处理流程

当采集源试采失败或出现异常时，系统提供三级修复能力：**自动诊断 → 一键修复 → 人工辅助修复**。

#### 第一步：发现异常

| 入口 | 查看方式 | 说明 |
|------|---------|------|
| 监控仪表盘 | 红色/黄色告警区域 | 最直观，优先看 |
| 采集源详情 | 智能诊断卡片（试采失败时自动显示） | 带诊断和自动修复功能 |
| 死信队列 | 采集管理 → 死信队列 | 重试耗尽，必须人工处理 |
| 任务列表 | 筛选「失败」状态 | 正在重试中的失败任务 |

#### 第二步：智能诊断与修复（推荐首选）

进入采集源详情页，当试采失败时会自动显示**「智能诊断」**卡片，提供以下操作：

**诊断分析**

点击「诊断分析」按钮，系统自动检测：
- URL 是否存在重定向（HTTP→HTTPS 等）
- 服务器是否返回 403（是否需要浏览器 UA 头）
- 页面是否为 JS 动态渲染（SPA/政务云平台）
- 是否存在底层 API 接口（JCMS、JPAAS 等）
- 页面中是否包含文章链接

诊断完成后显示结果标签和建议修复步骤。

**一键修复**

点击「一键修复」按钮，系统自动执行完整修复链（最多 10 步）：

| 步骤 | 操作 | 说明 |
|------|------|------|
| 1 | 诊断分析 | 分析网站状态和问题原因 |
| 2 | 修复URL | 如有重定向，自动更新为最终URL |
| 3 | 切换模板 | 根据诊断结果切换合适的采集模板 |
| 4 | 生成规则 | 调用 Worker 重新生成采集规则 |
| 5 | 首轮试采 | 用当前模板+规则测试采集 |
| 6 | 多模板轮换 | 首轮失败时，依次尝试 gov_cloud_platform、static_list、api_json 模板 |
| 7 | LLM 深度分析 | 所有模板均失败时，调用大模型深度分析网页的数据加载机制 |
| 8 | LLM 方案试采 | 用 LLM 发现的新采集方案进行试采 |
| 9 | 保存新模板 | LLM 方案成功时，自动保存为自定义模板供同类网站复用 |
| 10 | 评分 | 5 维度评分（列表数量、标题多样性、正文内容、正文长度、无乱码） |

修复完成后显示每步执行结果和评分变化（如：评分 0 → 0.4）。评分达到 0.4 即通过，采集源自动变为「试采通过」状态。

> 步骤 7-9 需要配置 LLM API Key。未配置时步骤 1-6 仍可正常执行。

**人工辅助修复**

当自动修复仍无法解决时，点击「人工辅助修复」按钮展开输入框，输入您对网页的人工分析结果，系统将结合您的线索由 LLM 自动生成采集规则并试采。

操作步骤：
1. 在浏览器打开目标网站，按 F12 打开开发者工具
2. 切换到 Network 标签，筛选 Fetch/XHR，刷新页面观察数据请求
3. 将发现的线索输入到输入框中，例如：
   - "文章列表通过 API 加载，接口是 https://xxx/api/list?page=1，返回 JSON"
   - "文章在 .news-list > li 容器中，a 标签包含链接和标题"
   - "页面通过 iframe 加载，iframe 地址是 /frame/list.html"
   - "需要带 Cookie 才能访问"
4. 点击「提交并自动调试」
5. 系统自动：LLM 分析线索 → 生成规则 → 切换模板 → 保存规则 → 试采 → 评分

> 提示越具体越好。最有价值的信息是：API 地址和参数、CSS 选择器、特殊请求头要求。

#### 第三步：按错误类型手动处理

如果以上自动修复方式均不适用，可参考错误类型手动处理：

| 错误类型 | 典型表现 | 推荐处理方式 |
|---------|---------|------------|
| 规则失效 | 列表页提取 0 篇文章 | 优先使用「一键修复」；或到规则编辑页点击「LLM生成」重新生成 |
| 网站改版 | 模板不匹配 | 采集源详情 → 重新检测 → 确认新模板 → 保存；或「一键修复」自动处理 |
| 反爬拦截 | HTTP 403 | 「一键修复」会自动尝试升级反爬策略；严重时需配置代理池 |
| 请求过频 | HTTP 429 | 在规则中增大 delay_min/delay_max（3-5秒），等待后重试 |
| 网络超时 | ConnectTimeout | 单个源直接重试；批量超时检查 Worker 网络（系统管理 → Worker 状态） |
| SSL 错误 | CERTIFICATE_VERIFY_FAILED | 等网站修复证书，标记忽略 |
| JS 渲染 | 页面为空壳，0 篇文章 | 启动 Browser Worker；或使用「人工辅助修复」提供 API 线索 |

#### 第四步：跟踪验证

1. 修复后查看详情页的试采结果和评分变化
2. 评分通过（>=0.4）→ 进入审核 → 批准上线
3. 建议修复后连续观察 2-3 个采集周期（4-6 小时）

#### 批量异常处理技巧

1. 死信队列按「错误类型」筛选，确认是否同类问题
2. 同域名/平台批量失效 → 先对一个源使用「一键修复」，确认有效后批量重试其他源
3. LLM 成功发现新采集方案时会自动保存为自定义模板（模板列表页可查看），同平台的其他源可直接复用该模板

