## 五、常见问题

**Q: 为什么某个采集源一直显示「异常」？**
A: 进入采集源详情查看最近日志，通常是网站改版导致规则失效，需到规则管理中 LLM 重新生成规则。

**Q: 队列积压过多怎么办？**
A: 检查 Worker 状态确认都在线。Worker 正常但积压仍多，需增加 Worker 实例数。

**Q: 如何暂停某个采集源？**
A: 采集源列表 → 点击名称进入详情 → 点击「暂停」。暂停后不再参与定时调度。

**Q: 导入的采集源为什么显示「待检测」？**
A: 新导入的源需完成自动检测（识别模板+生成规则）→ 试采验证 → 人工审核后才能上线。`

